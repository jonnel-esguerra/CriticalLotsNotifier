package com.onsemi.cim.tarlac.triggers.model;

public class ToldLotsInfo {
	
	private String stepName;
	private String lotNo;
	private String toldRule;
	private String toldRuleStatus;
	private String toldRuleMsg;
	private String toldRuleCreated;
	
	public String getStepName() {
		return stepName;
	}
	public void setStepName(String stepName) {
		this.stepName = stepName;
	}
	public String getLotNo() {
		return lotNo;
	}
	public void setLotNo(String lotNo) {
		this.lotNo = lotNo;
	}
	public String getToldRule() {
		return toldRule;
	}
	public void setToldRule(String toldRule) {
		this.toldRule = toldRule;
	}
	public String getToldRuleStatus() {
		return toldRuleStatus;
	}
	public void setToldRuleStatus(String toldRuleStatus) {
		this.toldRuleStatus = toldRuleStatus;
	}
	public String getToldRuleMsg() {
		return toldRuleMsg;
	}
	public void setToldRuleMsg(String toldRuleMsg) {
		this.toldRuleMsg = toldRuleMsg;
	}
	public String getToldRuleCreated() {
		return toldRuleCreated;
	}
	public void setToldRuleCreated(String toldRuleCreated) {
		this.toldRuleCreated = toldRuleCreated;
	}
	
	
	

}
